                            <h2>Servicio de Escaneo</h2>
                            <h3>
                              Permite realizar Auto diagnóstico completo del auto
                            </h3>
                            <p>
                              Los scanner tienen la función de detectar todas las unidades de control electrónico que se han instalado en el auto y puede leer los códigos de error en caso de que existan algunas fallas en el auto.
                            </p>
                            <h3>
                              Lector de identificación
                            </h3>
                            <p>
                              El scanner nos va mostrar la identificación total de la unidad de control; unos ejemplos, el fabricante, el número de partes que tiene el automóvil, la versión o marca del software, etc.
                            </p>
                            <h3>
                              La función principal es la de leer los códigos de error (check engine)
                            </h3>
                            <p>
                              Muestra la descripción completa de todos los errores almacenados (un ejemplo, " Falla en Sensor de oxigeno").
                            </p>
                            <h3>
                              Eliminar códigos de error almacenados
                            </h3>
                            <p>
                              Los errores se almacenan cuando existe alguna falla y esta función se encarga de eliminar todos los códigos de error encontrados y otros datos de la información de diagnóstico.
                            </p>
                            <h3>
                              Prueba de Actuadores
                            </h3>
                            <p>
                              Prueba de actuador especial (por ejemplo, bloquear / desbloquear las puertas, encender la bomba de combustible, corte de combustible, bloqueo / desbloqueo de TPMS rueda, etc. Todo esto depende de las opciones con la que cuenta el vehículo y el Scanner Automotriz).
                            </p>
                            <h3>
                              Medir los valores
                            </h3>
                            <p>
                              Una función importante para tener un conocimiento del estado del auto, es un programa de lectura de datos en directo, como sensor de oxígeno, muestra de la velocidad del motor, temperatura del refrigerante, tensión de batería, ajustes de combustible, etc. Estos valores pueden ser visualizados en un gráfico gracias al programa.
                            </p>

